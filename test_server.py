class DemoDHCPServerTest(unittest.TestCase):
    def test_toniebox_udhcpd(self):
        pool = demodhcpd.AddressPool([
            ipaddress.ip_address('10.54.42.211')
        ])
        server_ip = ipaddress.ip_address('10.54.42.1')
        dns_ip = ipaddress.ip_address('1.1.1.1')
        serv = demodhcpd.DHCPServer(
            server_ip, dns_ip, pool, 24,
            demodhcpd.craft_udhcpd_broken, test_log)

        answer, to = serv.handle(TONIEBOX_DISCOVER)
        self.assertEqual(answer, BROKEN_UDHCPD_OFFER)
        self.assertEqual(to, ('255.255.255.255', 68))

        answer, to = serv.handle(TONIEBOX_REQUEST)
        self.assertEqual(answer, BROKEN_UDHCPD_ACK)
        self.assertEqual(to, ('255.255.255.255', 68))

    def test_toniebox_isc(self):
        pool = demodhcpd.AddressPool([
            ipaddress.ip_address('10.54.42.100')
        ])
        server_ip = ipaddress.ip_address('10.54.42.1')
        serv = demodhcpd.DHCPServer(
            server_ip, server_ip, pool, 24,
            demodhcpd.craft_isc, test_log)

        answer, to = serv.handle(TONIEBOX_DISCOVER_BROADCAST)
        self.assertEqual(answer, ISC_OFFER_BROADCAST)
        self.assertEqual(to, ('255.255.255.255', 68))

        answer, to = serv.handle(TONIEBOX_DISCOVER_UNICAST)
        self.assertEqual(answer, ISC_OFFER_UNICAST)
        self.assertEqual(to, ('10.54.42.100', 68))

        answer, to = serv.handle(TONIEBOX_REQUEST_UNICAST)
        self.assertEqual(answer, ISC_ACK_UNICAST)
        self.assertEqual(to, ('10.54.42.100', 68))

